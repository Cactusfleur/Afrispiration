// Special aliases to Congo variants
// 'congo' already maps to CG, DRC maps to CD via the long name above.

export function countryNameToIso2(name: string): string | undefined {
  const n = norm(name)
  const code = MAP[n]
  // empty string used to explicitly skip flags (e.g., African Diaspora)
  if (code === "") {
    console.log("MISSING COUNTRY: ", name)
    return undefined
  }
  return code
}
